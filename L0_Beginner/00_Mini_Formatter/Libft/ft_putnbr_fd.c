#include "libft.h"
#include <unistd.h>

static void	ft_putcharfd(char c, int fd)
{
	write(fd, &c, 1);
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		ft_putcharfd('-', fd);
		ft_putnbr_fd(-n, fd);
	}
	else if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putcharfd(n % 10 + '0', fd);
	}
	else
	{
		ft_putcharfd(n + '0', fd);
	}
}

